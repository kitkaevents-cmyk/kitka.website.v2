const SUPPORTED_LANGS = ["en", "bg"];
const DEFAULT_LANG = "bg";

function detectInitialLang() {
  const stored = localStorage.getItem("kitka-lang");
  if (stored && SUPPORTED_LANGS.includes(stored)) return stored;
  const nav = (navigator.language || "en").toLowerCase();
  return nav.startsWith("bg") ? "bg" : DEFAULT_LANG === "bg" ? "bg" : "en";
}

function applyLang(lang) {
  const dict = translations[lang] || translations.en;
  document.documentElement.setAttribute("lang", lang);
  document.documentElement.setAttribute("data-lang", lang);

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key] !== undefined) el.textContent = dict[key];
  });

  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (dict[key] !== undefined) el.setAttribute("placeholder", dict[key]);
  });

  const titleKey = document.documentElement.getAttribute("data-title-key");
  if (titleKey && dict[titleKey]) document.title = dict[titleKey];

  const toggle = document.getElementById("langToggle");
  if (toggle) toggle.setAttribute("data-lang", lang);

  localStorage.setItem("kitka-lang", lang);
}

document.addEventListener("DOMContentLoaded", () => {
  applyLang(detectInitialLang());

  const toggle = document.getElementById("langToggle");
  if (toggle) {
    toggle.addEventListener("click", () => {
      const current = document.documentElement.getAttribute("data-lang") || "en";
      applyLang(current === "en" ? "bg" : "en");
    });
  }
});
